AiAgregator - описание функций 
веб-сайт-агрегатор с адаптивной сеткой услуг, функциями поиска и фильтрации, страницами с подробной информацией об услугах, с личным кабинетом пользователя, с фаворитами,  с настройками количества, процессом  сравнения услуг по выбранным критериям. Карусель с рекомендуемыми услугами на главной странице. 
1. Пользователь (User)
Возможности:
Поиск и сравнение предложений
Сохранение фильтров и избранных офферов
Получение персонализированных рекомендаций
Общение с AI-ассистентом
Просмотр и оставление отзывов
Регистрация и управление аккаунтом
Ограничения:
Нет доступа к партнёрским или административным данным
2. Партнёр (Partner)
Возможности:
Загрузка и управление своими предложениями
Получение аналитики по лидам, кликам и конверсиям
Доступ к white-label решениям
Просмотр статистики по своим кампаниям
Ограничения:
Доступ только к собственным данным и офферам
3. Модератор (Moderator)
Возможности:
Проверка и одобрение отзывов
Работа с жалобами и флагами
Редактирование контента по ролям
Ограничения:
Нет доступа к системным настройкам и пользовательским данным
4. Администратор (Admin)
Возможности:
Полный контроль над пользователями, правами, контентом
Доступ ко всем логам и системной информации
Управление категориями, фильтрами, тегами
Настройка политик безопасности и GDPR-функций
Ограничения:
Только вручную назначается через backend
5. Разработчик / Интегратор (Developer)
Возможности:
Доступ к API, песочнице, логам запросов
Генерация и управление API-ключами
Тестирование и отладка интеграций
Ограничения:
Нет доступа к пользовательским данным (только через тестовые аккаунты)
6. SEO-специалист (SEO Specialist)
Возможности:
Управление метаданными, контентом для SEO
Просмотр и анализ позиций, Schema.org, hreflang
Доступ к AI-рекомендациям по улучшению видимости
Ограничения:
Нет прав на изменение коммерческих или пользовательских данных
*******
Сбор данных из открытых источников (если нет API)
Подход:
AI-парсинг и извлечение данных:
Автоматический обход сайтов поставщиков (Scrapy, Playwright, Puppeteer).
Извлечение ключевых параметров (цены, условия, лимиты, бонусы) через LLM-модели (LangChain, HuggingFace).
Нормализация и сопоставление данных с предыдущими версиями.
Формирование структурированных объектов:
Преобразование всех данных в формат StandardizedProduct.
Версионирование офферов для отслеживания изменений.
AI-валидация:
Проверка данных на актуальность и корректность.
Выявление аномалий (завышенные цены, несоответствие рынку).
Обновление данных:
Планировщик (APScheduler / Celery Beat) для регулярной переиндексации.
Приоритет — популярным офферам и страницам с высокой активностью.
Интеллектуальный сбор и fallback-логика:
Агентные фреймворки (LangChain Agents, AutoGPT) для обхода, фильтрации, распознавания контента.
При невозможности извлечения данных — пользователь получает уведомление и может отправить заявку вручную.
AI обучается на новых источниках в реальном времени.
AI Data Flow (при отсутствии API):
flowchart TD
    A[Ссылка на сайт провайдера] --> B[Парсер (Playwright / Scrapy)]
    B --> C[LLM (NLP → JSON)]
    C --> D[AI-валидация условий]
    D --> E[StandardizedProduct]
    E --> F[БД / Кэш]
    F --> G[Пользователь / AI / Модуль сравнения]
